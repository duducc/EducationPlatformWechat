package com.saint.wechat.manager.bean;

public class MyxfEntity {
	private String holdUnit;
	private String coursesTask;
	private String eduCredit;
	private String state;
	private String courseid;
	
	public String getCourseid() {
		return courseid;
	}
	public void setCourseid(String courseid) {
		this.courseid = courseid;
	}
	public String getHoldUnit() {
		return holdUnit;
	}
	public void setHoldUnit(String holdUnit) {
		this.holdUnit = holdUnit;
	}
	public String getCoursesTask() {
		return coursesTask;
	}
	public void setCoursesTask(String coursesTask) {
		this.coursesTask = coursesTask;
	}
	public String getEduCredit() {
		return eduCredit;
	}
	public void setEduCredit(String eduCredit) {
		this.eduCredit = eduCredit;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	
}
